# Defining the function print_numbers
def print_numbers():
    # For loop to print the numbers 1 through 10. We use range to specify the numbers we want to print. The first number is the starting number, and the second number is the number we want to stop at. We want to stop at 11 because range does not include the last number in the range.
    for i in range(1,11):
        # Print the numbers 1 through 10.
        print(i)  
# Call the function print_numbers
print_numbers()
