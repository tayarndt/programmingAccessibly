# Description: This program will double a number
#           and print the result.
# Author:  Taylor Arndt
#  Define a     function called double_number that takes a number as a parameter.
def double_number(number):
    #  Return a doubled number.
    return number * 2
    
# Call the function with the number 8 as an argument.
result = double_number(8)
print(f"Our doubled number is {result}.")
